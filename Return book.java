    private void Returnbook_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Returnbook_buttonActionPerformed
        // TODO add your handling code here:

        // Get the book barcode entered by the user
        String bookBarcode = reurn_booksearchbar.getText();

        if (bookBarcode.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a book barcode.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            // Check if the book is eligible for returning
            if (isBookEligibleForReturn(bookBarcode)) {
                // Perform the return operation
                if (returnBook(bookBarcode)) {
                    JOptionPane.showMessageDialog(this, "Book with barcode " + bookBarcode + " has been returned.", "Success", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to return the book.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Book with barcode " + bookBarcode + " is not eligible for return.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_Returnbook_buttonActionPerformed

