    private boolean isBookEligibleForReturn(String bookBarcode) {
        String sql = "SELECT book_duedate, book_status FROM library_books WHERE book_barcode = ?";

        try ( PreparedStatement statement = connection.prepareStatement(sql)) {
            statement.setString(1, bookBarcode);
            ResultSet resultSet = statement.executeQuery();

            if (resultSet.next()) {
                java.sql.Date dueDate = resultSet.getDate("book_duedate");
                String bookStatus = resultSet.getString("book_status");

                return (dueDate != null) && ("checked out".equals(bookStatus));
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }

        return false;
    }

     /**
     * Performs the return operation for a book based on its barcode.
     *
     * @param bookBarcode The barcode of the book to be returned.
     * @return True if the return operation is successful, false otherwise.
     */
    private boolean returnBook(String bookBarcode) {
        String sql = "UPDATE library_books SET book_duedate = NULL, book_status = 'checked in' WHERE book_barcode = ?";

        try ( PreparedStatement statement = connection.prepareStatement(sql)) {
            statement.setString(1, bookBarcode);
            return statement.executeUpdate() > 0;
        } catch (SQLException e) {
            e.printStackTrace();
        }

        return false;
    }

